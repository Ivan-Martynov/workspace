function love.draw()
    for y, row in ipairs(level) do
        for x, cell in ipairs(row) do
            if cell ~= cell_types.empty then
                local colors = {
                    [cell_types.player] = {0.64, 0.5, 1},
                    [cell_types.player_on_storage] = {0.62, 0.4, 1},
                    [cell_types.box] = {1, 0.79, 0.49},
                    [cell_types.box_on_storage] = {0.59, 1, 0.5},
                    [cell_types.storage] = {0.61, 0.9, 1},
                    [cell_types.wall] = {1, 0.58, 0.82},
                }

                local pos_x = (x - 1) * cell_size
                local pos_y = (y - 1) * cell_size
                love.graphics.setColor(colors[cell])
                love.graphics.rectangle("fill", pos_x, pos_y,
                    cell_size, cell_size)

                love.graphics.setColor(1, 1, 1)
                love.graphics.print(level[y][x], pos_x, pos_y)
            end
        end
    end
