function love.keypressed(key)
    if key == "escape" then
        love.event.quit()
    elseif key == "r" then
        load_level()
    elseif key == "n" then
        current_level = current_level + 1
        if current_level > #levels then
            current_level = 1
        end
        load_level()
    elseif key == "p" then
        current_level = current_level - 1
        if current_level < 1 then
            current_level = #levels
        end
        load_level()
    elseif (key == "up") or (key == "down")
        or (key == "left") or (key == "right") then

        local dx, dy = 0, 0
        if key == "left" then
            dx = -1
        elseif key == "right" then
            dx = 1
        else
            dy = key == "up" and -1 or 1
        end

        local player_x
        local player_y

        for y, row in ipairs(level) do
            for x, cell in ipairs(row) do
                if (cell == cell_types.player)
                    or (cell == cell_types.player_on_storage) then
                    player_x = x
                    player_y = y
                end
            end
        end

        local current = level[player_y][player_x]
        local adjacent = level[player_y + dy][player_x + dx]
        local adjacent_cell = {
            [cell_types.empty] = cell_types.player,
            [cell_types.storage] = cell_types.player_on_storage,
        }

        local beyond
        if level[player_y + dy + dy] then
            beyond = level[player_y + dy + dy][player_x + dx + dx]
        end

        local next_beyond = {
            [cell_types.empty] = cell_types.box,
            [cell_types.storage] = cell_types.box_on_storage,
        }

        local next_current = {
            [cell_types.player] = cell_types.empty,
            [cell_types.player_on_storage] = cell_types.storage,
        }

        local next_adjacent_push = {
            [cell_types.box] = cell_types.player,
            [cell_types.box_on_storage] = cell_types.player_on_storage,
        }

        if adjacent_cell[adjacent] then
            level[player_y][player_x] = next_current[current]
            level[player_y + dy][player_x + dx] = adjacent_cell[adjacent]
        elseif next_beyond[beyond] and next_adjacent_push[adjacent] then
            level[player_y][player_x] = next_current[current]
            level[player_y + dy][player_x + dx] = next_adjacent_push[adjacent]
            level[player_y + dy + dy][player_x + dx + dx] = next_beyond[beyond]
        end

        local complete = true
        for _, row in ipairs(level) do
            for _, cell in ipairs(row) do
                if cell == cell_types.box then
                    complete = false
                end
            end
        end

        if complete then
            current_level = current_level + 1
            if current_level > #levels then
                current_level = 1
            end
            load_level()
        end
    end
end

